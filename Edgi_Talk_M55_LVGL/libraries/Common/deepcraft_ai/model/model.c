static inline int mtb_model_f32_to_quantized_int8(const void* handle, const float* restrict src, int src_count, void* restrict dst, int dst_count, int8_t* tmp)
{
	mtb_ml_model_t* model = *(mtb_ml_model_t**)handle;

	// Quantize float input to int8
	int in_offset = model->input_zero_point;
	float in_scale = model->input_scale;

	for (int i = 0; i < src_count; i++) {
		float value = (src[i] / in_scale) + in_offset;
		if (value > 127)
			tmp[i] = 127;
		else if (value < -128)
			tmp[i] = -128;
		else
			tmp[i] = (int8_t)value;
	}

	mtb_ml_model_run(model, (MTB_ML_DATA_T*)tmp);

	int ret_status = model->lib_error;
	if (ret_status != 0) { printf("ERROR: TensorFlow Lite model inference failed with error code: %d.\r\n", ret_status); }
	CY_ASSERT(ret_status == 0);

	memcpy(dst, model->output, dst_count);

	return IPWIN_RET_SUCCESS;
}

static inline void dequantize_d8_to_f32(const int8_t* restrict src, float* restrict dst, int count, float scale, int offset)
{
	while (count > 0U)
	{
		*dst++ = (*src++ - offset) * scale;
		count--;
	} 
}

static inline void mtb_model_free(const void* handle)
{
	mtb_ml_model_t* model = *(mtb_ml_model_t**)handle;
	mtb_ml_model_deinit(model);

#ifndef IMPRESSURE_NO_NPU
	mtb_ml_deinit();
#endif

	if (IMPRESSURE_mtb_models_count > 0)
		IMPRESSURE_mtb_models_count--;
}

static inline int mtb_model_soft_reset(const void* handle)
{
	mtb_ml_model_t* model = *(mtb_ml_model_t**)handle;

	cy_rslt_t result = mtb_ml_model_rnn_reset_all_parameters(model);

	if (result != MTB_ML_RESULT_SUCCESS)
		return IPWIN_RET_ERROR;

	return IPWIN_RET_SUCCESS;
}

